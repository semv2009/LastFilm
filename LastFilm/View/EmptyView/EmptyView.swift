import UIKit

protocol EmptyViewDelegate {
    func emptyViewDidClickButton(emptyView: EmptyView)
}

class EmptyView: UIView {

    var view: UIView!
    
    var nibName: String = "EmptyView"
    
    var delegate: EmptyViewDelegate?
    
    override init(frame: CGRect) {
        super.init(frame: frame)
        setup()
    }
    
    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
        setup()
    }
    
    func setup() {
        view = loadViewFromNib()
        view.frame = bounds
        view.autoresizingMask = [UIViewAutoresizing.flexibleWidth, UIViewAutoresizing.flexibleHeight]
        addSubview(view)
    }
    
    @IBAction func tryLoad(_ sender: Any) {
        delegate?.emptyViewDidClickButton(emptyView: self)
    }
    
    func loadViewFromNib() -> UIView {
        let bundle = Bundle(for: type(of: self))
        let nib = UINib(nibName: nibName, bundle: bundle)
        if let view = nib.instantiate(withOwner: self, options: nil)[0] as? UIView {
            return view
        }
        return UIView()
    }
    
}
